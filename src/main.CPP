#include <bits/stdc++.h>

using namespace std;

int main(int argc, char *argv[])
{
    // Enable input from file
    if (argc > 1)
    {
        freopen(argv[1], "r", stdin);
    }

    // Enable output from file
    if (argc > 2)
    {
        freopen(argv[2], "w", stdout);
    }

    char x;
    cin >> x;

    cout << x;

    return 0;
}